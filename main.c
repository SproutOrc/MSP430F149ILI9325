void Init_Clk(void)
{
    uint delay;
    BCSCTL1 &= ~XT2OFF;                    // XT2OFF:控制XT2振荡器的开启(XT2OFF=0)
    BCSCTL2 |= SELM_2 + SELS;               //SELS:选择SMCLK的时钟源,0:DCOCLK;1:XT2CLK.   
    for(delay=5000;delay>0;delay--);	   //SELM0~1:选择MCLK的时钟源,0,1:DCOCLK,2:XT2CLK,3:LFXT1CLK
            IFG1 &=~OFIFG;      			   //晶振故障中断标志位  
    TACCR0=0XFFFF; 
  
}

